    print(f"  {name}")
    print(f"  Accuracy : {acc:.4f}  |  ROC-AUC : {auc_sc:.4f}")
    print(f"{'='*54}")
    print(classification_report(y, y_pred, target_names=["Normal", "Botnet"]))
    return acc, auc_sc, y_pred, y_prob


