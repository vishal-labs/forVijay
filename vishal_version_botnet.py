import tensorflow as tf
from tensorflow.keras.models import Model
from tensorflow.keras.layers import Input, Dense, BatchNormalization, Dropout
from tensorflow.keras.callbacks import EarlyStopping
from sklearn.preprocessing import StandardScaler

# Use original features only (no engineered features) for the AE
X_train_ae_raw = train_df.drop("label", axis=1).drop(columns=ENGINEERED_FEATURES, errors='ignore')
X_test_ae_raw  = test_df.drop("label",  axis=1).drop(columns=ENGINEERED_FEATURES, errors='ignore')

y_train_ae = train_df["label"]
y_test_ae  = test_df["label"]

AE_FEATURE_COLUMNS = X_train_ae_raw.columns.tolist()

# StandardScaler + PCA (fixed 50 components) for the AE pipeline
scaler_ae = StandardScaler()
X_train_ae_sc = scaler_ae.fit_transform(X_train_ae_raw)
X_test_ae_sc  = scaler_ae.transform(X_test_ae_raw)

pca_ae = PCA(n_components=50, random_state=42)
X_train_ae_pca = pca_ae.fit_transform(X_train_ae_sc)
X_test_ae_pca  = pca_ae.transform(X_test_ae_sc)

# Train the AE on CLEAN NORMAL traffic only
X_train_normal = X_train_ae_pca[y_train_ae.values == 0]

q1   = np.percentile(X_train_normal, 5,  axis=0)
q3   = np.percentile(X_train_normal, 95, axis=0)
mask = np.all((X_train_normal >= q1) & (X_train_normal <= q3), axis=1)
X_train_clean  = X_train_normal[mask]

print(f"AE training samples (clean normal traffic): {X_train_clean.shape}")

# Build autoencoder
input_dim    = X_train_clean.shape[1]
inp          = Input(shape=(input_dim,))

x            = Dense(128, activation="relu")(inp)
x            = BatchNormalization()(x)
x            = Dropout(0.2)(x)
x            = Dense(64, activation="relu")(x)
x            = BatchNormalization()(x)
bottleneck   = Dense(32, activation="relu")(x)     # compressed bottleneck

x            = Dense(64,  activation="relu")(bottleneck)
x            = Dense(128, activation="relu")(x)
out          = Dense(input_dim, activation="linear")(x)

autoencoder  = Model(inp, out)
autoencoder.compile(optimizer="adam", loss="mae")
autoencoder.summary()

early_stop = EarlyStopping(monitor="val_loss", patience=5, restore_best_weights=True)

history = autoencoder.fit(
    X_train_clean, X_train_clean,
    epochs          = 50,
    batch_size      = 256,
    validation_split= 0.1,
    shuffle         = True,
    callbacks       = [early_stop],
    verbose         = 1,
)

# Reconstruction errors (log-normalised MAE)
def recon_error(X):
    recon = autoencoder.predict(X, verbose=0)
    return np.log1p(np.maximum(np.mean(np.abs(X - recon), axis=1), 0))


mse_train = recon_error(X_train_clean)
mse_test  = recon_error(X_test_ae_pca)

threshold = np.percentile(mse_train, 97)
print("Autoencoder anomaly threshold:", threshold)

ae_predictions = (mse_test > threshold).astype(int)

print("\nAutoencoder Classification Report:")
print(classification_report(y_test_ae, ae_predictions, target_names=["Normal", "Botnet"]))
print(confusion_matrix(y_test_ae, ae_predictions))


def predict_autoencoder(input_dict: dict) -> dict:
    """
    Predict via autoencoder anomaly score.
    Returns: {'prediction': 'BOTNET'|'NORMAL', 'anomaly_score': float}
    """
    df    = pd.DataFrame([input_dict])
    df    = pd.get_dummies(df)
    df    = df.reindex(columns=AE_FEATURE_COLUMNS, fill_value=0)
    X     = scaler_ae.transform(df)
    X     = pca_ae.transform(X)
    error = float(recon_error(X)[0])

    return {
        "prediction":    "BOTNET" if error > threshold else "NORMAL",
        "anomaly_score": round(error, 6),
    }


