def explain_prediction(input_dict: dict, top_n: int = 10) -> pd.Series:
    """
    Print the top contributing features for a single prediction.

    Usage:
        result = predict_botnet(my_sample)
        explain_prediction(my_sample)
    """
    X_input   = prepare_input(input_dict)            # → DataFrame, aligned to FEATURE_COLUMNS
    X_pca_inp = transform_pipeline(lgb_model, X_input)  # → ndarray (1, n_components)

    sv_raw = explainer.shap_values(X_pca_inp)
    if isinstance(sv_raw, list):
        sv_pca = sv_raw[1]        # (1, n_components)
    else:
        sv_pca = sv_raw

    sv_orig  = backproject_shap(sv_pca, pca_lgb)     # (1, n_features)
    contrib  = pd.Series(sv_orig[0], index=FEATURE_COLUMNS)

    prob = float(lgb_model.predict_proba(X_input)[0][1])
    pred = "BOTNET" if prob > 0.6 else ("NORMAL" if prob < 0.45 else "SUSPICIOUS")

    print(f"\n{'─'*58}")
    print(f"  Prediction : {pred}   (confidence: {prob:.4f})")
    print(f"  Base value : {base_val:.4f}  → final log-odds: {base_val + contrib.sum():.4f}")
    print(f"{'─'*58}")
    print(f"  {'Feature':<30} {'SHAP':>9}   Direction")
    print(f"  {'─'*54}")
    for feat in contrib.abs().nlargest(top_n).index:
        val   = contrib[feat]
        arrow = "▲ → BOTNET" if val > 0 else "▼ → NORMAL"
        print(f"  {feat:<30} {val:>+9.4f}   {arrow}")
    print()

    # Mini waterfall
    exp = shap.Explanation(
        values        = sv_orig[0],
        base_values   = base_val,
        data          = X_input.iloc[0].values,
        feature_names = FEATURE_COLUMNS,
    )
    shap.waterfall_plot(exp, max_display=10, show=True)
    return contrib


