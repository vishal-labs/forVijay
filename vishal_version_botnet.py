def prepare_input(input_dict: dict) -> pd.DataFrame:
    """
    Convert a raw network-flow dict into a model-ready DataFrame.
    Steps: OHE → add engineered features → align to training columns.
    """
    df = pd.DataFrame([input_dict])
    df = pd.get_dummies(df)        # OHE categorical fields
    df = add_features(df)          # engineered features (safe_log applied)
    df = df.reindex(columns=FEATURE_COLUMNS, fill_value=0)
    return df


def predict_botnet(input_dict: dict) -> dict:
    """
    Predict botnet vs. normal traffic with a 3-class output and confidence.
    Returns: {'prediction': 'BOTNET'|'SUSPICIOUS'|'NORMAL', 'confidence': float}
    """
    X    = prepare_input(input_dict)
    prob = float(ensemble_model.predict_proba(X)[0][1])

    if prob > 0.60:
        prediction = "BOTNET"
    elif prob < 0.45:
        prediction = "NORMAL"
    else:
        prediction = "SUSPICIOUS"

    return {"prediction": prediction, "confidence": round(prob, 4)}


