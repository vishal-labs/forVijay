import shap
import numpy as np
import pandas as pd
import matplotlib.pyplot as plt

# ── Helper: extract scaled+PCA-transformed data from a pipeline ──
def transform_pipeline(pipeline, X: pd.DataFrame) -> np.ndarray:
    """Push X through scaler and PCA steps only (not the classifier)."""
    X_scaled = pipeline.named_steps['scaler'].transform(X)
    X_pca    = pipeline.named_steps['pca'].transform(X_scaled)
    return X_pca


def backproject_shap(shap_pca: np.ndarray, pca_step) -> np.ndarray:
    """
    Map SHAP values from PCA space → original feature space.
    shap_pca:   (n_samples, n_components)
    pca_step:   fitted PCA object  [components_ shape: (n_components, n_features)]
    returns:    (n_samples, n_features)
    """
    return shap_pca @ pca_step.components_


